DECLARE
    V_DEPTNO DEPT.DEPTNO%TYPE := 10;
    V_DNAME DEPT.DNAME%TYPE;
BEGIN
    IF V_DEPTNO = 10 THEN
        V_DNAME := 'ACCOUNTING';
    ELSIF V_DEPTNO = 20 THEN
        V_DNAME := 'RESEARCH';
    ELSIF V_DEPTNO = 30 THEN
        V_DNAME := 'SALES';
    ELSIF V_DEPTNO = 40 THEN
        V_DNAME := 'OPERATIONS';
    ELSE
        V_DNAME := 'N/A';
    END IF;
    
    DBMS_OUTPUT.PUT_LINE('DNAME : ' || V_DNAME);
END;

